    private void comboButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboButtonActionPerformed
        // TODO add your handling code here:
            if((baseGame.getMoney())>=baseGame.getUpgradesList().get(3).getCost(1)){
            baseGame.removeMoney(baseGame.getUpgradesList().get(3).getCost(1));
            baseGame.getUpgradesList().get(3).buyUpgrade(1);
            moneyBox.setText("gold: " + Integer.toString(baseGame.getMoney()));
            comboDescription.setText(baseGame.getUpgradesList().get(3).getDescription()+baseGame.getUpgradesList().get(3).getCost(1));
            if(baseGame.getUpgradesList().get(3).getTimesBought()>=3){
                comboButton.setEnabled(false);
            }
            
        }
    }//GEN-LAST:event_comboButtonActionPerformed

