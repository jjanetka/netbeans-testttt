    private void DamageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DamageActionPerformed
        // TODO add your handling code here:
        if(canParse(damageField.getText())){
            if((baseGame.getMoney())>=baseGame.getUpgradesList().get(0).getCost(Integer.parseInt(damageField.getText()))){
            baseGame.removeMoney(baseGame.getUpgradesList().get(0).getCost(Integer.parseInt(damageField.getText())));
            baseGame.getUpgradesList().get(0).buyUpgrade(Integer.parseInt(damageField.getText()));
            moneyBox.setText("gold: " + Integer.toString(baseGame.getMoney()));
            }
        }
    }//GEN-LAST:event_DamageActionPerformed

